<template>
  <div>
    <el-table :data="memberList" style="width: 100%">
      <el-table-column prop="uid" label="用户编号" width="180">
      </el-table-column>

      <el-table-column prop="nickname" label="手机号" width="180">
      </el-table-column>

      <el-table-column prop="phone" label="昵称" width="180">
      </el-table-column>

      <el-table-column prop="status" label="状态">
        <template slot-scope="item">
          <div>
            <el-tag v-if="item.row.status == 1" type="success">启用</el-tag>
            <el-tag v-else type="danger">禁用</el-tag>
          </div>
        </template>
      </el-table-column>

      <el-table-column label="操作">
        <template slot-scope="item">
          <div>
            <el-button type="info" plain @click="edit(item.row.uid)"
              >编辑</el-button
            >
           
          </div>
        </template>
      </el-table-column>
    </el-table>
  </div>
</template>

<script>
import { mapActions, mapGetters } from "vuex";
export default {
  data() {
    return {};
  },

  computed: {
    ...mapGetters({
      memberList: "member/getMemberList",
    }),
  },

  mounted() {
    //组件一加载触发行动
    this.getMemberAction();
  },

  methods: {
    ...mapActions({
      getMemberAction: "member/getMemberListAction",
    }),
  
    edit(id) {
      this.$emit("edit", id);
    },
  },
};
</script>

